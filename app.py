def confidence_label(prob_parkinsons: float, predicted_label: str):
    confidence = prob_parkinsons if predicted_label == "Parkinson's" else (1 - prob_parkinsons)
    if confidence >= 0.85:
        return confidence, "High"
    elif confidence >= 0.70:
        return confidence, "Moderate"
    else:
        return confidence, "Low"


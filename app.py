def get_events_and_event_id(raw):
    if raw.annotations is not None and len(raw.annotations) > 0:
        try:
            events, event_id = mne.events_from_annotations(raw, verbose="error")
            return events, event_id, "annotations"
        except Exception:
            pass

    try:
        events = mne.find_events(raw, verbose="error")
        event_id = {str(int(x)): int(x) for x in np.unique(events[:, 2])}
        return events, event_id, "find_events"
    except Exception:
        return np.empty((0, 3), dtype=int), {}, "none"


def parse_target_from_events_tsv(events_tsv_path):
    if events_tsv_path is None or not Path(events_tsv_path).exists():
        return None

    try:
        df = pd.read_csv(events_tsv_path, sep="\t")
    except Exception:
        return None

    if "trial_type" not in df.columns:
        return None

    target_rows = df[df["trial_type"].astype(str).str.contains("Target Tone", case=False, na=False)]
    if len(target_rows) == 0:
        return None

    for col in ["value", "stim_file", "trial_type"]:
        if col in target_rows.columns:
            vals = target_rows[col].dropna().astype(str).unique().tolist()
            for v in vals:
                if "200" in v or "target" in v.lower():
                    return v

    return "Target Tone"


def find_target_annotation_keys(event_id, events_tsv_target_hint=None):
    keys = list(event_id.keys())
    matches = []

    for k in keys:
        ks = str(k).strip()
        ks_low = ks.lower().replace(" ", "")

        if "s200" in ks_low or "targettone" in ks_low or "target" in ks_low:
            matches.append(k)

    if events_tsv_target_hint is not None:
        hint = str(events_tsv_target_hint).strip().lower().replace(" ", "")
        for k in keys:
            ks_low = str(k).strip().lower().replace(" ", "")
            if hint in ks_low and k not in matches:
                matches.append(k)

    return matches


def epoch_target_only(raw, events_tsv_path=None):
    events, event_id, source = get_events_and_event_id(raw)

    if len(events) == 0:
        raise ValueError("No events were found in the uploaded EEG recording.")

    target_hint = parse_target_from_events_tsv(events_tsv_path)
    target_keys = find_target_annotation_keys(event_id, target_hint)

    if len(target_keys) == 0:
        raise ValueError(
            f"Could not identify the target event from event_id keys: {list(event_id.keys())}"
        )

    target_key = target_keys[0]
    target_code = event_id[target_key]

    picks = mne.pick_types(raw.info, meg=False, eeg=True, eog=False, exclude="bads")

    epochs = mne.Epochs(
        raw,
        events,
        event_id={"target": target_code},
        tmin=TMIN,
        tmax=TMAX,
        baseline=BASELINE,
        preload=True,
        picks=picks,
        verbose="error"
    )

    if len(epochs) == 0:
        raise ValueError(f"No target epochs created for target key: {target_key}")

    ptp = np.ptp(epochs.get_data(), axis=2)
    keep = ~np.any(ptp > REJECT_PTP_EVENT, axis=1)
    epochs = epochs[np.where(keep)[0]]

    if len(epochs) == 0:
        raise ValueError("All target epochs were rejected by the peak-to-peak threshold.")

    return epochs, event_id, source, target_key, target_code



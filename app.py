def save_uploaded_files(set_file, fdt_file=None, events_tsv_file=None):
    temp_dir = Path(tempfile.mkdtemp(prefix="eeg_upload_"))

    set_path = temp_dir / Path(set_file.name).name
    with open(set_path, "wb") as f:
        f.write(set_file.getbuffer())

    fdt_path = None
    if fdt_file is not None:
        fdt_path = temp_dir / f"{set_path.stem}.fdt"
        with open(fdt_path, "wb") as f:
            f.write(fdt_file.getbuffer())

    events_tsv_path = None
    if events_tsv_file is not None:
        events_tsv_path = temp_dir / Path(events_tsv_file.name).name
        with open(events_tsv_path, "wb") as f:
            f.write(events_tsv_file.getbuffer())

    return set_path, fdt_path, events_tsv_path



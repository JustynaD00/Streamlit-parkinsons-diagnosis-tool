def subject_level_prediction(model, X_epoch, threshold=SUBJECT_THRESHOLD):
    epoch_prob = model.predict_proba(X_epoch)[:, 1]
    mean_prob = float(np.mean(epoch_prob))
    pred = int(mean_prob >= threshold)
    label = "Parkinson's" if pred == 1 else "Control"
    return epoch_prob, mean_prob, pred, label



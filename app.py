def standardise_names(raw):
    rename_map = {}
    for ch in raw.ch_names:
        clean = ch.strip()
        upper = clean.upper()

        if upper == "T3":
            clean = "T7"
        elif upper == "T4":
            clean = "T8"
        elif upper == "FP1":
            clean = "Fp1"
        elif upper == "FP2":
            clean = "Fp2"
        elif upper == "FZ":
            clean = "Fz"
        elif upper == "FCZ":
            clean = "FCz"
        elif upper == "CZ":
            clean = "Cz"
        elif upper == "PZ":
            clean = "Pz"

        rename_map[ch] = clean

    raw.rename_channels(rename_map)
    return raw


def pick_target_channels(raw):
    available = [ch for ch in TARGET_CHANNELS if ch in raw.ch_names]
    missing = [ch for ch in TARGET_CHANNELS if ch not in raw.ch_names]

    if not available:
        raise ValueError("None of the expected EEG channels were found.")

    raw = raw.copy().pick(available)
    return raw, available, missing


def detect_and_interpolate_bad_channels(raw):
    raw = raw.copy().load_data()
    eeg_raw = raw.copy().pick("eeg")
    eeg_data = eeg_raw.get_data()
    ch_names = eeg_raw.ch_names
    n_ch = len(ch_names)

    if n_ch == 0:
        return raw, []

    protected = {ch.upper() for ch in BAD_CHAN_EOG_PROTECT}
    bad_channels = []

    channel_sds = np.std(eeg_data, axis=1)

    flat_mask = channel_sds < BAD_CHAN_FLAT_THRESH
    bad_channels.extend([ch for ch, bad in zip(ch_names, flat_mask) if bad and ch.upper() not in protected])

    if n_ch >= 4:
        zscores = (channel_sds - np.mean(channel_sds)) / (np.std(channel_sds) + 1e-12)
        noisy_mask = zscores > BAD_CHAN_NOISY_Z_THRESH
        bad_channels.extend([ch for ch, bad in zip(ch_names, noisy_mask) if bad and ch.upper() not in protected])

    bad_channels = sorted(list(set(bad_channels)))
    max_bad = int(np.floor(BAD_CHAN_MAX_FRACTION * n_ch))

    if len(bad_channels) > max_bad:
        raise ValueError(f"Too many bad channels detected ({len(bad_channels)}/{n_ch}).")

    if bad_channels:
        raw.info["bads"] = bad_channels
        raw.interpolate_bads(reset_bads=True, verbose="error")

    return raw, bad_channels


def apply_reference(raw):
    raw = raw.copy()
    try:
        raw.set_eeg_reference("average", projection=False, verbose="error")
    except Exception:
        pass
    return raw


def apply_filters(raw, notch_freq=DEFAULT_NOTCH_FREQ):
    raw = raw.copy().load_data()

    if notch_freq < raw.info["sfreq"] / 2:
        raw.notch_filter([notch_freq], picks="eeg", verbose="error")

    raw.filter(BANDPASS[0], BANDPASS[1], picks="eeg", verbose="error")
    return raw


def ensure_sfreq(raw, target_sfreq=RESAMPLE_SFREQ):
    raw = raw.copy()
    if abs(float(raw.info["sfreq"]) - target_sfreq) > 1e-6:
        raw.resample(target_sfreq, verbose="error")
    return raw


def fit_and_apply_ica(raw):
    raw_for_ica = raw.copy().filter(l_freq=ICA_HP, h_freq=None, picks="eeg", verbose="error")

    n_channels = len(raw_for_ica.ch_names)
    n_components = min(n_channels, max(2, int(np.floor(ICA_NCOMP * n_channels))))

    ica = ICA(
        n_components=n_components,
        random_state=42,
        method="fastica",
        max_iter="auto"
    )
    ica.fit(raw_for_ica, verbose="error")

    exclude = []
    eog_candidates = [ch for ch in ["Fp1", "Fp2"] if ch in raw_for_ica.ch_names]

    for eog_ch in eog_candidates:
        try:
            inds, _ = ica.find_bads_eog(raw_for_ica, ch_name=eog_ch, verbose="error")
            exclude.extend(inds[:1])
        except Exception:
            pass

    exclude = sorted(list(set(exclude)))[:2]

    raw_clean = raw.copy()
    ica.apply(raw_clean, exclude=exclude)
    return raw_clean, exclude



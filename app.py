def safe_divide(a, b):
    return np.divide(a, b, out=np.zeros_like(a), where=(b != 0))


def compute_bandpower_maps(data, sfreq, bands):
    band_dict = {}

    n_times = data.shape[-1]
    n_per_seg = min(int(sfreq), n_times)
    n_fft = min(512, max(128, n_times))
    n_overlap = n_per_seg // 2 if n_per_seg >= 4 else 0

    for band_name, (fmin, fmax) in bands.items():
        psd_out = mne.time_frequency.psd_array_welch(
            data,
            sfreq=sfreq,
            fmin=fmin,
            fmax=fmax,
            n_per_seg=n_per_seg,
            n_fft=n_fft,
            n_overlap=n_overlap,
            average="mean",
            verbose=False
        )

        if isinstance(psd_out, tuple):
            psd, _ = psd_out
        else:
            psd = psd_out

        band_dict[band_name] = psd.mean(axis=-1)

    return band_dict


def extract_clean_features_from_epochs(epochs):
    available_channels = [ch for ch in SELECTED_CHANNELS if ch in epochs.ch_names]
    missing = [ch for ch in SELECTED_CHANNELS if ch not in epochs.ch_names]

    if len(available_channels) < len(SELECTED_CHANNELS):
        raise ValueError(f"Missing selected SVM channels: {missing}")

    epochs = epochs.copy().pick(SELECTED_CHANNELS)
    data = epochs.get_data().astype(np.float32)
    sfreq = float(epochs.info["sfreq"])
    ch_names = epochs.ch_names
    times = epochs.times

    feature_blocks = []
    feature_names = []

    band_dict = compute_bandpower_maps(data, sfreq, BANDS)

    for band_name in BANDS.keys():
        bp = band_dict[band_name]
        feature_blocks.append(bp)
        feature_names.extend([f"{ch}_{band_name}_abs" for ch in ch_names])

    total_power = np.zeros_like(next(iter(band_dict.values())))
    for band_name in BANDS.keys():
        total_power += band_dict[band_name]

    for band_name in BANDS.keys():
        rel = safe_divide(band_dict[band_name], total_power)
        feature_blocks.append(rel)
        feature_names.extend([f"{ch}_{band_name}_rel" for ch in ch_names])

    ratio_defs = {
        "theta_beta_ratio": ("theta", "beta"),
        "alpha_theta_ratio": ("alpha", "theta"),
        "alpha_beta_ratio": ("alpha", "beta"),
    }
    for ratio_name, (num_band, den_band) in ratio_defs.items():
        ratio = safe_divide(band_dict[num_band], band_dict[den_band])
        feature_blocks.append(ratio)
        feature_names.extend([f"{ch}_{ratio_name}" for ch in ch_names])

    for win_name, t1, t2 in ERP_WINDOWS:
        idx = np.where((times >= t1) & (times <= t2))[0]
        if len(idx) == 0:
            raise ValueError(f"No samples found for ERP window {win_name}.")
        win_mean = data[:, :, idx].mean(axis=2)
        feature_blocks.append(win_mean)
        feature_names.extend([f"{ch}_{win_name}_mean" for ch in ch_names])

    global_blocks = [
        ("global_delta_rel", safe_divide(band_dict["delta"], total_power).mean(axis=1, keepdims=True)),
        ("global_theta_rel", safe_divide(band_dict["theta"], total_power).mean(axis=1, keepdims=True)),
        ("global_alpha_rel", safe_divide(band_dict["alpha"], total_power).mean(axis=1, keepdims=True)),
        ("global_beta_rel",  safe_divide(band_dict["beta"], total_power).mean(axis=1, keepdims=True)),
    ]

    for name, arr in global_blocks:
        feature_blocks.append(arr.astype(np.float32))
        feature_names.append(name)

    X = np.concatenate(feature_blocks, axis=1).astype(np.float32)
    X = np.nan_to_num(X, nan=0.0, posinf=0.0, neginf=0.0)

    return X, feature_names


